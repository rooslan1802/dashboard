const YEAR = 2026;

const typeByKind = {
  office: 'office',
  home: 'home',
  trip: 'business_trip',
  dayOff: 'day_off',
  deep: 'office',
};

const iso = (month, day, time = '10:00') => {
  const [hour, minute] = time.split(':').map(Number);
  return new Date(YEAR, month - 1, day, hour, minute || 0).toISOString();
};

const hours = (start, end) => Math.max(0, (new Date(end) - new Date(start)) / 36e5);

const moodForHours = (value) => {
  if (value >= 10) return 'drained';
  if (value >= 8.5) return 'tired';
  if (value <= 4) return 'normal';
  return 'focused';
};

const makeSession = ({ month, day, start = '10:00', end = '18:00', type = 'office', notes = '' }) => {
  const startTime = iso(month, day, start);
  const endTime = type === 'dayOff' ? null : iso(month, day, end);
  const totalHours = endTime ? hours(startTime, endTime) : 0;

  return {
    id: crypto.randomUUID(),
    start_time: startTime,
    end_time: endTime,
    total_hours: Number(totalHours.toFixed(2)),
    work_type: typeByKind[type],
    mood: type === 'dayOff' ? 'normal' : moodForHours(totalHours),
    notes,
    created_at: startTime,
    is_manual_edit: true,
    is_active_session: false,
  };
};

